    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtNombre.getText().equalsIgnoreCase("") || txtSexo.getText().equalsIgnoreCase("") || txtEdad.getText().equalsIgnoreCase("") || txtEC.getText().equalsIgnoreCase("") || txtOcupacion.getText().equalsIgnoreCase("") || txtTelCasa.getText().equalsIgnoreCase("") || txtDomicilio.getText().equalsIgnoreCase("") || txtCel.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null,"un campo esta vacio, favor de rellenar");
        }else{
            guardado=true;
            setedad(Integer.parseInt(txtEdad.getText()));
            setnombre(txtNombre.getText());
            setsexo(txtSexo.getText());
            
            try{
                Connection con=null;
                con = BD.getConection();
                
                PreparedStatement ps;
                ResultSet res;
                
                ps=con.prepareStatement("CALL Insertar_Paciente('"+txtNombre.getText()+"','"+txtSexo.getText()+"',"+Integer.parseInt(txtEdad.getText())+",'"+txtEC.getText()+"','"+txtOcupacion.getText()+"','"+txtTelCasa.getText()+"','"+txtDomicilio.getText()+"','"+txtCel.getText()+"','"+txtMotivo.getText()+"')");
                res=ps.executeQuery();
                
                ps=con.prepareStatement("CALL retorno_id('"+getnombre()+"',"+getedad()+")");
                res=ps.executeQuery();
                res.next();
                setid(res.getInt("idPaciente"));
                
                if(edad<18){
                    ps=con.prepareStatement("CALL insertar_infante("+getid()+")");
                    res=ps.executeQuery();
                }
                
            }catch(Exception e){
                System.out.print(e);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        if (guardado==true){
            if(Integer.parseInt(txtEdad.getText())>=18){
                Agregar_Paciente_Adulto v1=new Agregar_Paciente_Adulto();
                v1.setVisible(true);
                this.setVisible(false);
            } else {
                Agregar_Paciente_Infante v1=new Agregar_Paciente_Infante();
                v1.setVisible(true);
                this.setVisible(false);
            }
        }else{
            JOptionPane.showMessageDialog(null,"No ha guardado");
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed
    
